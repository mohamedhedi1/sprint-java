/**
 * FXML Controller class
 *
 * @author PC
 */
public class SportConnectFrontController implements Initializable {

    @FXML
    private Button logout;

    /**
     * Initializes the controller class.
     */
    @Override
    public void initialize(URL url, ResourceBundle rb) {
        // TODO
    }    

    @FXML
    private void logout(ActionEvent event) {
        try {
            Session.getInstance().logout();
            FXMLLoader loader = new FXMLLoader(getClass().getResource("loginF.fxml"));
            Parent root = loader.load();
            Scene scene = new Scene(root);
            Stage stage = new Stage();
            stage.setScene(scene);
            stage.show();
            
            
            ((Node)(event.getSource())).getScene().getWindow().hide();      // Fermer la fenÃªtre actuelle
        } catch (IOException ex) {
            Logger.getLogger(SportConnectBackController.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
}
